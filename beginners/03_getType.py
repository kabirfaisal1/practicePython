# This program is used to demonstrate the syntax of getting variables type in python.
print("In python, we use type() function to get the data type of a variable. Type is executed by the interpreter.");

x = 14;
print("In python x=5 will be integer because type(x) a integer value:", type(x));
y = "Kabir";
print("In python y = Kabir will be string because type(y) a string value:", type(y));
z= 3.14;
print("In python z = 3.14 will be float because type(z) a float value:", type(z));